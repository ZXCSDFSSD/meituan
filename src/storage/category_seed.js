// ── 写入 category_mapping 表（幂等）────────────────────────────────────────
// 注意：SQLite UNIQUE(raw_name, store_id) 在 store_id=NULL 时不阻止重复插入
// （SQL 标准：NULL != NULL，所以每个 NULL 行都被视为独立），
// 因此改为：先按 raw_name 去重，检查数量是否一致，不一致则清空重写。

async function seedCategoryMapping(log = console.log) {
    // 按 raw_name 去重（数组中可能有重复条目）
    const deduped = new Map(CATEGORY_MAPPING.map(([r, s]) => [r, s]));

    const cur = await db.queryOne('SELECT COUNT(*) cnt FROM category_mapping WHERE store_id IS NULL');
    if (cur.cnt === deduped.size) {
        log(`✅ 品类映射已就绪 (${cur.cnt} 条)`);
        return 0;
    }

    // 清除旧全局映射，重新写入
    await db.run('DELETE FROM category_mapping WHERE store_id IS NULL');
    let inserted = 0;
    for (const [rawName, standardName] of deduped) {
        try {
            await db.run(
                `INSERT INTO category_mapping (raw_name, standard_name, store_id) VALUES (?, ?, NULL)`,
                [rawName, standardName]
            );
            inserted++;
        } catch (e) {
            log(`⚠️  category_mapping 写入失败 [${rawName}]: ${e.message}`);
        }
    }
    log(`✅ 品类映射写入完成: 共 ${inserted} 条`);
    return inserted;
}

/** 查询标准品类名（如无映射则返回原始名） */
async function resolveStandardCategory(rawName) {
    const row = await db.queryOne(
        `SELECT standard_name FROM category_mapping WHERE raw_name = ? AND store_id IS NULL LIMIT 1`,
        [rawName]
    );
    return row ? row.standard_name : rawName;
}

module.exports = { seedCategoryMapping, resolveStandardCategory, CATEGORY_MAPPING };
